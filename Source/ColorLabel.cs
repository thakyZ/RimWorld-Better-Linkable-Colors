namespace Drummeur.BetterLinkableColors;
/// <summary>
/// An enum representing different states for the colors.
/// </summary>
public enum ColorLabel
{
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.black" />
    /// </summary>
    BLACK = 0,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.blue" />
    /// </summary>
    BLUE = 1,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.cyan" />
    /// </summary>
    CYAN = 2,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.green" />
    /// </summary>
    GREEN = 3,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.grey" />
    /// </summary>
    GREY = 4,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.magenta" />
    /// </summary>
    MAGENTA = 5,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.red" />
    /// </summary>
    RED = 6,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.white" />
    /// </summary>
    WHITE = 7,
    /// <summary>
    /// Uses the <see cref="UnityEngine.Color.yellow" />
    /// </summary>
    YELLOW = 8,
    ///// <summary>
    ///// Uses a custom <see cref="UnityEngine.Color" />
    ///// </summary>
    //CUSTOM = 9,
};
